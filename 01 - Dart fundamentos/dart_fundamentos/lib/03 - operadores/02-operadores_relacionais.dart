void main(){
  //Operadores relacionais
  // == (igualdade)
  // != (diferença)
  // > (maior que)
  // < (menor que )
  // <= (menor igual)
  // >= (maior igual)

  final idade = 19;

  if(idade == 18){
    print("idade é igual (==) 18");
  }
  else if (idade != 18){
    print("idade diferente de 18");
  }
  else if (idade > 18){
    print("idade maior de 18");
  }
  else if (idade < 18){
    print("idade menor de 18");
  }
  else if (idade <= 18){
    print("idade menor igual de 18");
  }
  else if(idade >= 18){
    print("idade maior igual de 18");
  }
}